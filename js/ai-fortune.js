    const hour = parseInt(document.getElementById('aiHour').value);
    const question = document.getElementById('aiQuestion').value.trim();
    const apiKey = document.getElementById('aiApiKey').value.trim();

    if (!name) { alert('请输入姓名'); return; }
    if (year < 1900 || year > 2100) { alert('年份需在 1900-2100 之间'); return; }
    if (day > daysInMonth(year, month)) { alert('日期超出当月天数'); return; }

    hideEl('aiForm');
    showEl('aiResult');
    const content = document.getElementById('aiResultContent');
    content.innerHTML = '<p class="loading-text">🔮 正在为你深度解读命运密码…</p>';

