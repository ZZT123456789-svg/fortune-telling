  judgeStrength: function(r) {
    var dm = r.dmElement;
    var allGan = [r.yearP.gan, r.monthP.gan, r.dayP.gan, r.hourP.gan];
    var allZhi = [r.yearP.zhi, r.monthP.zhi, r.dayP.zhi, r.hourP.zhi];
    var sameCount = 0, shengCount = 0, keCount = 0;
    var sheng={木:'水',火:'木',土:'火',金:'土',水:'金'};
    var ke={木:'金',火:'水',土:'木',金:'火',水:'土'};

    // 统计天干
    allGan.forEach(function(g){
      var e = BaziClassics._stemElement(g);
      if (e===dm) sameCount++;
      else if (sheng[e]===dm) shengCount++;
      else if (ke[e]===dm) keCount++;
    });

    // 统计地支藏干（简化）
    allZhi.forEach(function(z){
      var h = BaziClassics._zhiHidden(z);
      h.forEach(function(hg){
        var e = BaziClassics._stemElement(hg);
        if (e===dm) sameCount+=0.5;
        else if (sheng[e]===dm) shengCount+=0.5;
      });
    });

    var total = sameCount + shengCount*0.7 + keCount*(-0.5);
    var level, desc;
    if (total>=4) {level='身强';desc='日主'+dm+'元素在全局占绝对优势，精力充沛，抗压能力强，适合独立承担重任。喜克泄耗（官杀、财星、食伤）以求平衡。';}
    else if (total>=2.5) {level='身偏强';desc='日主'+dm+'元素较为有力，有一定抗压能力和主见。遇克泄耗之运可有大成。';}
    else if (total>=1.5) {level='中和';desc='日主'+dm+'不旺不弱，命局平衡度较好。逢生扶运则进，逢克泄运则守。';}
    else if (total>=1) {level='身偏弱';desc='日主'+dm+'偏弱，需借印比帮身。适合团队合作，借平台发展。';}
    else {level='身弱';desc='日主'+dm+'较弱，能量有限，需谨慎管理精力。喜印星和比劫帮扶，不宜独立承担过重责任。';}
    return {level:level,desc:desc,total:total,same:sameCount,sheng:shengCount};
  },

